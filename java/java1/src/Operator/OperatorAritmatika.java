package Operator;

public class OperatorAritmatika {
    
    public static void main(String[] args) {
        System.out.println("Operasi Penjumlahan");

        int Hasilpenjumlahan = 5 + 1;

        System.out.println("Hasil 5 + 1 = " + Hasilpenjumlahan);
        System.out.println();

        System.out.println("Operasi Pengurangan");

        int HasilPengurangan = 10 - 7;

        System.out.println("Hasil 10 - 7 = " + HasilPengurangan);
        System.out.println();

        System.out.println("Operasi Perkalian");
        
        int Hasilperkalian = 5 * 5;

        System.out.println("Hasil 5 * 5 = " + Hasilperkalian);
        System.out.println();

        System.out.println("Operasi Pembagian");

        int Hasilpembagi = 8/2;

        System.out.println("Hasil 8/2 = " + Hasilpembagi);
        System.out.println();

        System.out.println("Operasi Habis Bagi");

        int Hasilsisa = 10%2;

        System.out.println("Hasil 10 % 2 = " +Hasilsisa);
        System.out.println();

        System.out.println("Hasil sisa lain");

        int Hasilsisalain = 9%2;

        System.out.println("Hasil lain 9 % 2 = " + Hasilsisalain);
        System.out.println();
    }
}
